    def set_board_size(self, dimensions):
        ''' Sets the board with a given set of dimensions'''

        board = []
        for row in range(0, dimensions[1]):
            board.append([])
            for _ in range(0, dimensions[0]):
                board[row].append(0)

        # Update Attributes
        self.board = board
        self.width = dimensions[0]
        self.height = dimensions[1]

        # Return new board        
        return board
    
