    def reset_board(self):
        ''' Clear the board of all components. Board returns as all blank'''

        for row in range(0, self.height):
            for col in range(0, self.width):
                if self.board[row][col] != 0:
                    self.board[row][col] = 0

        self.start = None
        self.goal = None

