        x, y = coord[0], coord[1]

        # Reset original goal coordinates to blank
        if self.goal:
            self.board[self.goal[0]][self.goal[1]] = 0
            
        # Change Goal Coodinates
        self.goal = coord
        self.board[x][y] = 3

